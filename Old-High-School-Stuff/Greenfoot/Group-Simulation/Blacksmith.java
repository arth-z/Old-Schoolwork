import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

/**
 * A skeleton class representing a still image of a blacksmith.
 * 
 * @author Steven Lu and Arthur Zeng
 * @version 2.00
 */
public class Blacksmith extends Building
{
    
    private GreenfootImage blacksmith = new GreenfootImage("blacksmith.png");
    
    /**
     * Sets its image.
     */
    public Blacksmith(){
        this.setImage(blacksmith);
    }
}
