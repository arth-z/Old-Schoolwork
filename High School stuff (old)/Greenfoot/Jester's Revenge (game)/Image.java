import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

/**
 * Dummy class that holds an image.
 * 
 * @author Michael Liu
 * @version 1.0
 */
public class Image extends UI
{
   
    /**
     * Accepts an image as parameter. Displays that image.
     * 
     * @param sprite            the image you want to display.
     */
    public Image(GreenfootImage sprite){
        this.setImage(sprite);
    }
   
}
