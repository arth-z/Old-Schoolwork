import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

/**
 * Skeleton class, decoration. An image of a road.
 * 
 * @author Steven Lu 
 * @version 1.00
 */
public class Road extends Decoration
{
    GreenfootImage Road = new GreenfootImage("road.png");
    
    /**
     * Sets the image of the actor to a road.
     */
    public Road(){
        this.setImage(Road);
    }
}
