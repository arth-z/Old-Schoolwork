import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

/**
 * A skeleton class representing a still image of a Town Hall.
 * 
 * @author Steven Lu
 * @version 3.00
 */
public class TownHall extends Building
{
    
    private GreenfootImage townHall = new GreenfootImage("TownHall.png");
    /**
     * Sets its image to a town hall.
     */
    public TownHall(){
        this.setImage(townHall);
    }

}
